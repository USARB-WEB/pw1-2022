console.log("For");
for (let index = 1; index <= 10; index++) {
    console.log(index);
}
//console.log(index);

console.log("While");
let index = 1;
while (index <= 10) {
    console.log(index);
    index++;
}

console.log("Do While");
index = 1;
do {
    console.log(index);
    index++;
}while (index <= 10);